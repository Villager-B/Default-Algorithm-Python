def depth_1(x, y, count):
    if maze[x][y] == 1:
        print("goal count : ", count)
        return 0

    maze[x][y] = 2

    if maze[x - 1][y] < 2:
        depth_1(x - 1, y, count + 1)
    if maze[x + 1][y] < 2:
        depth_1(x + 1, y, count + 1)
    if maze[x][y + 1] < 2:
        depth_1(x, y + 1, count + 1)
    if maze[x][y - 1] < 2:
        depth_1(x, y - 1, count + 1)



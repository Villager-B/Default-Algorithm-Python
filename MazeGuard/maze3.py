def main():
    direct = [[1, 0], [0, 1], [-1, 0], [0, -1]]

    x, y, count, d = 1, 1, 0, 0

    while maze[x][y] != 1:
        maze[x][y] = 2

        for i in range(len(direct)):
            j = (d + i - 1) % len(direct)
            if maze[x + direct[j][0]][y + direct[j][1]] < 2:
                x += direct[j][0]
                y += direct[j][1]
                d = j
                count += 1
                break
            elif maze[x + direct[j][0]][y + direct[j][1]] == 2:
                x += direct[j][0]
                y += direct[j][1]
                d = j
                count -= 1
                break

    print(count)


